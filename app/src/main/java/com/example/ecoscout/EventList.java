package com.example.ecoscout;

public class EventList {
    String name, date;
    int location, desc;
    int image;

    public EventList(String name, String date, int location, int desc, int image) {
        this.name = name;
        this.date = date;
        this.location = location;
        this.desc = desc;
        this.image = image;
    }
}
